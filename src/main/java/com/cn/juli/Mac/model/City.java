package com.cn.juli.Mac.model;

public class City {

	private int cityId;
	private String cityName;
	private int obuEsamStruct;
	private int obuSecretVersion;

	public int getCityId() {
		return cityId;
	}

	public void setCityId(int cityId) {
		this.cityId = cityId;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

	public int getObuEsamStruct() {
		return obuEsamStruct;
	}

	public void setObuEsamStruct(int obuEsamStruct) {
		this.obuEsamStruct = obuEsamStruct;
	}

	public int getObuSecretVersion() {
		return obuSecretVersion;
	}

	public void setObuSecretVersion(int obuSecretVersion) {
		this.obuSecretVersion = obuSecretVersion;
	}
}
